package com.menic.other;

public class ListaFormas {
	private String opcao;
	private String qtdeOpcao;
	
	
	
	
	
	public ListaFormas(String opcao) {
		super();
		this.opcao = opcao;
		this.qtdeOpcao = "";
	}
	public String getOpcao() {
		return opcao;
	}
	public void setOpcao(String opcao) {
		this.opcao = opcao;
	}
	public String getQtdeOpcao() {
		return qtdeOpcao;
	}
	public void setQtdeOpcao(String qtdeOpcao) {
		this.qtdeOpcao = qtdeOpcao;
	}
	
	
	

}
